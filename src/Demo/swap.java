package Demo;

public class swap {
	public static void main(String[] args) {
		int a=10,b=20;
		System.out.println("Elements before swapping:");
		System.out.println("Value in a:"+a);
		System.out.println("Value in b:"+b);
		int temp=a;
		a=b;
		b=temp;
		System.out.println("Elements swapping:");
		System.out.println("Value in a:"+a);
		System.out.println("Value in b:"+b);
		
		
	}

}
