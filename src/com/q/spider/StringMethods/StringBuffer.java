package com.q.spider.StringMethods;

public class StringBuffer {
	

	public static void main(String[] args) {
		StringBuffer str1=new StringBuffer();
		StringBuffer str2=new StringBuffer();
		System.out.println("Before changes making:");
		System.out.println("value present in str1:"+str1);
		System.out.println("value present in str2:"+str2);
	
		System.out.println("After changes making:");
		System.out.println("value present in str1:"+str1);
		System.out.println("value present in str2:"+str2);
	
		
		
		
	}

}